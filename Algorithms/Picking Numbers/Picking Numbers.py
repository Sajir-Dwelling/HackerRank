def pickingNumbers(a):
    # Write your code here
    a.sort()
    max_length = 0
    i = 0

    for j in range(len(a)):
        while abs(a[j]-a[i])>1:
            i+=1
        max_length = max(max_length,j-i+1)
    return max_length

